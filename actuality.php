              <h4>L’ENVIRONNEMENT, UNE AFFAIRE POUR TOUS: Mercredi 29.11.2023</h4>
              
              <p> 
                « Pas d’environnement sans nous » en swahili, « Mazingira si bila sisi » est le thème de l’année pastorale 2023-2024 dans l’archidiocèse de Lubumbashi, situé dans la partie sud-est de la République démocratique du Congo. À travers ce thème, l’ordinaire du lieu, Mgr Fulgence Muteba, lance un appel à cultiver et développer la sensibilité vis-à-vis de la question environnementale. L’attention de toute la chrétienté de l’archidiocèse de Lubumbashi est donc focalisée sur la sauvegarde de « notre maison commune ». Ce thème s’inscrit dans la droite ligne de l’appel universel lancé par le pape François déjà en 2015 lorsqu’il publiait sa lettre Encyclique « Laudato Si’ : Sur la protection de notre maison commune », la toute première encyclique
                papale entièrement consacrée à la crise de la planète. Depuis, des actions ont vu le jour pour concrétiser cet appel et entreprendre un chemin de conversion écologique. On peut notamment citer la création du mouvement écologique, Laudato Si’, opérationnel au niveau mondial et dont le but est d’« Inspirer et de mobiliser la communauté catholique afin de prendre soin de notre maison commune et d’obtenir la justice climatique et écologique, en collaborant avec toutes les personnes de bonne volonté. » En Rd Congo, ce mouvement est présent et s’engage dans plusieurs activités qui relayent le message du souverain pontife au sujet de la préservation de l’environnement. L’archidiocèse de Lubumbashi n’est pas en reste. Le thème pastoral de cette année est un signal fort du désir de promouvoir une culture de l’écologie intégrale responsable. Soucieux d’atteindre jusqu’à la dernière couche de la population de sa juridiction, Mgr Fulgence Muteba encourage le mouvement Laudato Si’ de sensibiliser les fidèles des différentes paroisses.
                C’est dans cette perspective que le mouvement Laudato Si’ Lubumbashi sous le leadership de la sœur Thumba Sylvie, membre de la congrégation des sœurs salvatoriennes a animé une séance d’Information, Formation et Action (IFA) à l’attention des fidèles du Doyenné Kassapa accompagnés de leurs pasteurs. Chaque paroisse de ce doyenné a été représentée par quelques fidèles qui, au terme de cette formation sont devenus des potentiels membres des cercles Laudato Si’ dans leurs paroisses respectives. La paroisse et Aumônerie Universitaire a été choisie pour abriter cette rencontre qui s’est tenue le mercredi 29 novembre 2023 de 15h30 à 17h30. 
                L’intervention de la sœur Sylvie avait pour but celui de Laudato Si’ : entrainer les participants dans une démarche de conversion écologique qui implique avant tout le changement du cœur ; réduire l’empreinte, d’où l’expression ‘moins c’est plus’ qui veut dire que si chacun fait en sorte de réduire son impact négatif sur l’environnement cela constituerait un plus dans la réparation de ce dernier ; élever une voix prophétique pour dénoncer les injustices environnementales et climatiques.
                Elle a en outre souligné que la foi chrétienne avec ses valeurs et sa richesse était capitale dans la démarche de la préservation de la nature. En avocate de la protection de l’environnement au niveau de l’archidiocèse de Lubumbashi, la sœur Sylvie a aussi invité les participants à savoir prendre soin les uns des autres, à rester éveillés dans l’annonce en s’appropriant la question environnementale.
                Au titre d’actions pratiques, l’intervenante a encouragé à faire bon usage de poubelle, à végétaliser nos espaces de vie ce, en plantant un arbre chaque fois que l’on célèbre son anniversaire, à faire usage utile de la lumière, etc.
                En concluant son propos, elle a lancé quelques questions de réflexion : <br>
                1.	Comment montrons-nous que c’est Dieu qui nous a donné la terre, la pluie et les récoltes, l’univers ? <br>
                2.	Comment entant que chrétien participer à la protection de l’environnement dans un pays où on ne respecte pas la loi ? <br>
                3.	Comment devons-nous rendre notre paroisse, CEV un lieu de la protection de l’environnement ? <br>
                L’IFA s’est clôturée par une action écologique concrète : deux arbres ont été plantés dans l’enceinte de la paroisse Saint Esprit : le premier a été baptisé Charles Kazembe, du nom de l’abbé Curé-Doyen, actuellement curé de la Paroisse Marie Reine des Apôtres et le second, François Buando, curé de la paroisse hôte. Une photo de famille a complètement bouclé ce beau rendez-vous écologique. <br> <br><strong>Père Jean-Paul KAMBA, SJ</strong> 
           
